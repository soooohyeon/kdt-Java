package section02;
//3. 자료형 타입 - 문자형 ③ : (유니코드)문자형 타입으로 정수 출력하기
public class Type07 {
	public static void main(String[] args) {
		int alphabetA = 'A';	// 정수형 타입 변수에 유니코드인 A의 숫자가 담김
		int alphabetB = 'B';	// 정수형 타입 변수에 유니코드인 B의 숫자가 담김
		System.out.println(alphabetA);	
		System.out.println(alphabetB);
		
		char alphabetC = 67;	// 문자형 타입 변수에 유니코드인 67의 문자가 담김	
		System.out.println(alphabetC);
	}
}
