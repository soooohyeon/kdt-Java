package section02;
//3. 자료형 타입 - 문자열(참조형 변수)
public class Type05 {
	public static void main(String[] args) {
		String hi = "안녕하세요,";						// 문장열 변수를 선언하고 값 담기
		String niceToMeetYou = "만나서 반갑습니다.";	// 문장열 변수를 선언하고 값 담기
//		문자열은 참조 자료형이나 기본 자료형과 같이 문자열 타입으로 선언하고 사용
		System.out.println(hi);						// 변수에 담긴 문자열을 출력하고 줄바꿈
		System.out.println(niceToMeetYou);			// 변수에 담긴 문자열을 출력하고 줄바꿈
	}
}
