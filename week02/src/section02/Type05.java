package section02;
//3. 자료형 타입 - 문자형 ①
public class Type05 {
	public static void main(String[] args) {
		char ga = '가';			// 문자형 타입의 변수에 값 대입
		char na = '나';
		char alphabetA = 'A';
		char alphabetB = 'B';
		
		System.out.println(ga);		// () 안 변수의 값을 출력하고 줄바꿈함
		System.out.println(na);
		System.out.println(alphabetA + ", " + alphabetB);	// () 안 변수의 값을 문자열로 연결해서 출력하고 줄바꿈함
	}
}
